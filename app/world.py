"""Read spell and mob lists from the prepared world for autocomplete.

The world is a Bylins MUD `small/` tree (KOI8-R), generated by the build
stage from `lib/` + `lib.template/`. We scan it once on first request and
cache in memory; the world doesn't change at runtime.

- Spells: `cfg/spells.xml` -- regex-grep over `<name rus="..." eng="..."/>`
  with `mode="kEnabled"` filtering. Doing a full XML parse is overkill
  for one tag and complicates the KOI8-R decoding.
- Mobs: `world/zones/*/mobs/*.yaml`, looking up `vnum:` and
  `names.aliases`/`names.nominative` for the display name.
"""

from __future__ import annotations

import os
import re
from concurrent.futures import ThreadPoolExecutor
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable

import yaml as pyyaml

try:
    # libyaml C bindings -- 5-10x faster than the pure-Python loader, and
    # crucially releases the GIL during parse so ThreadPoolExecutor can
    # actually parallelise across files.
    from yaml import CSafeLoader as _SafeLoader  # type: ignore[attr-defined]
except ImportError:  # pragma: no cover -- fallback if libyaml isn't installed
    from yaml import SafeLoader as _SafeLoader

_PARSE_WORKERS = max(2, (os.cpu_count() or 4))


@dataclass(frozen=True)
class Spell:
    rus: str
    eng: str

    def matches(self, query: str) -> bool:
        q = query.lower()
        return q in self.rus.lower() or q in self.eng.lower()


@dataclass(frozen=True)
class Mob:
    vnum: int
    name: str

    def matches(self, query: str) -> bool:
        q = query.lower()
        return q in self.name.lower() or q in str(self.vnum)


@dataclass(frozen=True)
class Apply:
    location: int
    location_name: str  # human-readable, e.g. "сила"
    modifier: int


@dataclass(frozen=True)
class Obj:
    vnum: int
    name: str
    obj_type: str  # kWeapon, kArmor, ...
    wear_flags: tuple[str, ...]
    applies: tuple[Apply, ...] = ()
    affect_flags: tuple[str, ...] = ()  # kHaste, kStoneHands, kInvisible, ...
    extra_flags: tuple[str, ...] = ()

    def matches(self, query: str) -> bool:
        q = query.lower()
        return q in self.name.lower() or q in str(self.vnum)

    def fits_slot(self, slot: str) -> bool:
        # slot is one of the keys in SLOT_FLAGS below (e.g. "wield"); we
        # check that *any* of the required wear-flags is present on the
        # item. Empty slot filter -> "any wearable".
        if not slot:
            return True
        flags = SLOT_FLAGS.get(slot, ())
        return any(f in self.wear_flags for f in flags)


