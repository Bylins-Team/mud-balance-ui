_spells_cache: list[Spell] | None = None
_mobs_cache: list[Mob] | None = None
_objs_cache: list[Obj] | None = None

_SPELL_NAME_RE = re.compile(
    rb'<name\s+rus="([^"]+)"\s+eng="([^"]+)"\s*/>'
)
_SPELL_DISABLED_RE = re.compile(rb'mode="kDisabled"')


def load_spells(world_dir: Path) -> list[Spell]:
    """Walk cfg/spells.xml, return enabled spells. Order preserved.

    The XML lives in KOI8-R; we read raw bytes and decode each captured
    chunk separately so a malformed tag elsewhere can't crash the load.
    """
    global _spells_cache
    if _spells_cache is not None:
        return _spells_cache

    path = world_dir / "cfg" / "spells.xml"
    if not path.is_file():
        _spells_cache = []
        return _spells_cache

    raw = path.read_bytes()
    # Split on `<spell ` to walk per-block; this lets us skip disabled
    # entries cleanly without a real XML parser.
    out: list[Spell] = []
    seen: set[str] = set()
    for block in raw.split(b"<spell ")[1:]:
        if _SPELL_DISABLED_RE.search(block):
            continue
        m = _SPELL_NAME_RE.search(block)
        if not m:
            continue
        try:
            rus = m.group(1).decode("koi8-r")
            eng = m.group(2).decode("koi8-r")
        except UnicodeDecodeError:
            continue
        if rus.startswith("!") or rus in seen:
            continue
        seen.add(rus)
        out.append(Spell(rus=rus, eng=eng))

    out.sort(key=lambda s: s.rus.lower())
    _spells_cache = out
    return out


def _parse_yaml_file(path: Path):
    """Open a KOI8-R YAML file and return parsed data, or None on error."""
    try:
        with path.open("rb") as fh:
            return pyyaml.load(fh.read().decode("koi8-r"), Loader=_SafeLoader)
    except Exception:  # noqa: BLE001
        return None


def load_mobs(world_dir: Path) -> list[Mob]:
    """Walk world/zones/*/mobs/*.yaml; index vnum + display name."""
    global _mobs_cache
    if _mobs_cache is not None:
        return _mobs_cache

    files: list[Path] = []
    zones_dir = world_dir / "world" / "zones"
    if zones_dir.is_dir():
        files = sorted(zones_dir.glob("*/mobs/*.yaml"))

    out: list[Mob] = []
    seen_vnums: set[int] = set()
    if files:
        with ThreadPoolExecutor(max_workers=_PARSE_WORKERS) as ex:
            for data in ex.map(_parse_yaml_file, files):
                if not isinstance(data, dict):
                    continue
                for entry in _iter_mob_entries(data):
                    vnum = entry.get("vnum")
                    if not isinstance(vnum, int) or vnum in seen_vnums:
                        continue
                    names = entry.get("names") or {}
                    name = (
                        names.get("aliases")
                        or names.get("nominative")
                        or "?"
                    )
                    if isinstance(name, str):
                        name = name.strip().splitlines()[0] if name else "?"
                    else:
                        name = "?"
                    seen_vnums.add(vnum)
                    out.append(Mob(vnum=vnum, name=name))

    out.sort(key=lambda m: m.vnum)
    _mobs_cache = out
    return out


def _iter_mob_entries(data: dict) -> Iterable[dict]:
    """Mob YAML files come in two shapes: a single mob (top-level vnum/names)
    or `{mobs: [...]}` from a zone bundle."""
    if "vnum" in data:
        yield data
        return
    mobs = data.get("mobs")
    if isinstance(mobs, list):
        for m in mobs:
            if isinstance(m, dict):
                yield m


def search_spells(world_dir: Path, query: str, limit: int = 25) -> list[Spell]:
    if not query:
        return load_spells(world_dir)[:limit]
    return [s for s in load_spells(world_dir) if s.matches(query)][:limit]


def search_mobs(world_dir: Path, query: str, limit: int = 25) -> list[Mob]:
    if not query:
        return load_mobs(world_dir)[:limit]
    return [m for m in load_mobs(world_dir) if m.matches(query)][:limit]


# Types that the engine actually equips at the wear-flag slot. Other kHands
# items like kWorm ("заточка / закалка" -- unimplemented in the enum, used as
# crafting-only) carry the kHands flag but EquipObj rejects them via
# invalid_no_class. We skip them in the autocomplete to spare users from
# selecting kit that just falls into inventory.
WEARABLE_OBJ_TYPES = {
    "kLightSource", "kWeapon", "kArmor", "kContainer",
    "kLightArmor", "kMediumArmor", "kHeavyArmor",
}


def load_objects(world_dir: Path) -> list[Obj]:
    """Walk world/zones/*/objects/*.yaml; index vnum + name + wear_flags.

    Same KOI8-R yaml format as mobs. Items without a wear_flags entry, or
    with only `kTake`, or whose `type` is not in WEARABLE_OBJ_TYPES are
    skipped -- we only care about kit the engine will actually equip.
    """
    global _objs_cache
    if _objs_cache is not None:
        return _objs_cache

    files: list[Path] = []
    zones_dir = world_dir / "world" / "zones"
    if zones_dir.is_dir():
        files = sorted(zones_dir.glob("*/objects/*.yaml"))

    out: list[Obj] = []
    seen: set[int] = set()
    if files:
        with ThreadPoolExecutor(max_workers=_PARSE_WORKERS) as ex:
            for data in ex.map(_parse_yaml_file, files):
                if not isinstance(data, dict):
                    continue
                for entry in _iter_obj_entries(data):
                    vnum = entry.get("vnum")
                    if not isinstance(vnum, int) or vnum in seen:
                        continue
                    wear = entry.get("wear_flags") or []
                    if not isinstance(wear, list):
                        continue
                    # Skip items that can only be picked up but not equipped.
                    wearable = [w for w in wear if isinstance(w, str) and w != "kTake"]
                    if not wearable:
                        continue
                    names = entry.get("names") or {}
                    name = (
                        (names.get("aliases") if isinstance(names, dict) else None)
                        or (entry.get("short_desc") if isinstance(entry.get("short_desc"), str) else None)
                        or "?"
                    )
                    if isinstance(name, str):
                        name = name.strip().splitlines()[0] if name else "?"
                    else:
                        name = "?"
                    obj_type = entry.get("type") if isinstance(entry.get("type"), str) else "?"
                    if obj_type not in WEARABLE_OBJ_TYPES:
                        continue
                    applies_raw = entry.get("applies") or []
                    applies: list[Apply] = []
                    if isinstance(applies_raw, list):
                        for ap in applies_raw:
                            if not isinstance(ap, dict):
                                continue
                            loc = ap.get("location")
                            mod = ap.get("modifier")
                            if not isinstance(loc, int) or not isinstance(mod, int) or mod == 0:
                                continue
                            applies.append(Apply(
                                location=loc,
                                location_name=APPLY_NAMES.get(loc, f"apply{loc}"),
                                modifier=mod,
                            ))
                    aff_raw = entry.get("affect_flags") or []
                    aff = tuple(f for f in aff_raw if isinstance(f, str)) if isinstance(aff_raw, list) else ()
                    extra_raw = entry.get("extra_flags") or []
                    extra = tuple(f for f in extra_raw if isinstance(f, str)) if isinstance(extra_raw, list) else ()
                    seen.add(vnum)
                    out.append(Obj(
                        vnum=vnum,
                        name=name,
                        obj_type=obj_type,
                        wear_flags=tuple(wearable),
                        applies=tuple(applies),
                        affect_flags=aff,
                        extra_flags=extra,
                    ))

    out.sort(key=lambda o: o.vnum)
    _objs_cache = out
    return out


def _iter_obj_entries(data: dict):
    if "vnum" in data:
        yield data
        return
    objs = data.get("objects") or data.get("objs")
    if isinstance(objs, list):
        for o in objs:
            if isinstance(o, dict):
                yield o


def search_objects(world_dir: Path, slot: str, query: str, limit: int = 25) -> list[Obj]:
    items = [o for o in load_objects(world_dir) if o.fits_slot(slot)]
    if query:
        items = [o for o in items if o.matches(query)]
    return items[:limit]


def get_object(world_dir: Path, vnum: int) -> Obj | None:
    """O(N) lookup over the cached list -- N=17k, fine for occasional UI calls."""
    for o in load_objects(world_dir):
        if o.vnum == vnum:
            return o
    return None


def obj_summary(o: Obj) -> str:
    """Compact one-line description of an item's bonuses, used for tooltips."""
    parts: list[str] = []
    for ap in o.applies:
        sign = "+" if ap.modifier >= 0 else ""
        parts.append(f"{sign}{ap.modifier} {ap.location_name}")
    for f in o.affect_flags:
        parts.append(AFFECT_FLAG_LABELS.get(f, f))
    return ", ".join(parts) if parts else "—"
